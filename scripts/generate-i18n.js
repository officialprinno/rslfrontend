function setNested(obj, keyPath, value) {
  const parts = keyPath.split('.');
  let cur = obj;
  for (let i = 0; i < parts.length - 1; i++) {
    cur[parts[i]] = cur[parts[i]] || {};
    cur = cur[parts[i]];
  }
  cur[parts[parts.length - 1]] = value;
}

function buildBundle(flatExtra, navFlat) {
  const out = {};
  const navKeys = new Set([...uniqueKeys, ...Object.keys(navFlat)]);
  for (const key of navKeys) {
    const val = navFlat[key];
    if (val) {
      setNested(out, key, val);
    }
  }
  for (const [key, val] of Object.entries(flatExtra)) {
    setNested(out, key, val);
  }
  return out;
}

